package Model;

public class Suppliers {
int sup_id;
String sup_name;
String sup_addr;
long sup_cont;
String sup_sellprod;
public int getSup_id() {
	return sup_id;
}
public void setSup_id(int sup_id) {
	this.sup_id = sup_id;
}
public String getSup_name() {
	return sup_name;
}
public void setSup_name(String sup_name) {
	this.sup_name = sup_name;
}
public String getSup_addr() {
	return sup_addr;
}
public void setSup_addr(String sup_addr) {
	this.sup_addr = sup_addr;
}
public long getSup_cont() {
	return sup_cont;
}
public void setSup_cont(long sup_cont) {
	this.sup_cont = sup_cont;
}
public String getSup_sellprod() {
	return sup_sellprod;
}
public void setSup_sellprod(String sup_sellprod) {
	this.sup_sellprod = sup_sellprod;
}

}
